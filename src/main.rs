fn main() {

    let a: i32 = 10;
    let b: i32 = 15;
    println!("Hello, world! , {} {}", a, b);


    let character: char = 'a';
    println!("Character => {}", character);

    let boolean: bool = true;
    println!("Boolean => {}", boolean);

    let tuple: (i32, i32, f64, i32, bool) = (1, -2, 3.0, 4, true);
    println!("Tuple => {:?}", tuple);


}
